package oops.polymorphism.methodOverriding;

public class MethodOverriding {
	
	/*
	
	 ==========================Method Overriding===============================================
	 
	 -> use to achieve Polymorphism.
	 
	 -> There must be IS-A relationship between two classes.
	 
	 -> If both parent and child class  have same method(same name, same return type, same list of parameters)
	  	but different implementation then we can do method overriding.
	 
	 -> inherit => use same as parent
	 -> override => do modification/change in parent class's method in child class.
	 
	 
	 						Syntax:
	 						
	 				class A{
	 				
	 					void add(int a, int b){
	 						
	 						//parent class logics
	 					
	 					}
	 				}
	 				
	 				class B extends A{
	 				
	 					@Overriding
	 					void add(int a, int b){
	 					
	 							//child class logics
	 						
	 					}
	 					
	 				}
	 
	 */

}
