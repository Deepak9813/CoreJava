package oops.polymorphism.methodOverloading;

public class MethodOverloadingDocs {
	
	/*
	
	=========================================Method Overloading====================================================
	
	
		-> If a class has multiple methods having same name but different in parameters, it is  known as
		     method overloading.
	
			   OR[next definition]
		   Method Overloading is a concept(technique) of having more than one method with same name 
		    but different parameter list inside a class(same class). 
		
	    		
	    	===================== 3 ways to Overload method =====================
	    	
	    		In order to overload methods, the argument list(parameters) of the methods must differ in either of these:
	    		
	    		1) Number of parameter:
	    				for example: 
	    				 		 add(int, int)
	    						 add(int, int, int)
	    						 
	    						  
	    		2) Data type of parameter:
	    				for example:
	    						add(int , int)
	    						add(int, float)
	    						
	    		3) Sequence of Data type of parameters:
	    				for example:
	    						add(int, float)
	    						add(float, int)
	
	
	 */

}
