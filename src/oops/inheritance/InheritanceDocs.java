package oops.inheritance;

public class InheritanceDocs {
	
	/*
	 =====================================Inheritance=================================
	 	# Code re-usability
	 	# process of accessing properties and methods of parent/super/base class to child/sub/derived class is known as inheritance.
	 	# Also known as IS-A relationship. [eg: two class :- Cow , Animal ==> Cow is a Animal].
	 	# Its a generalization to specialization process.
	 	# 'extends' keyword is used in inheritance.
	 	# We can inherit only one class at a time.
	 	# We can inherit static variables and methods.
	 	
	 	# We can't inherit private properties and methods.
	 	# We can't inherit constructors.
	 	
	 	
	 				Syntax
	 		class child_class extends parent_class
	 		{
	 		
	 				//properties: parent + child
	 				 
	 				 //methods: parent + child
	 				 
	 				 
	 		}		 
	 		
	 		
	 		example:
	 	
	 	class A			//Grandfather
	 	{
	 	
	 	
	 	}
	 	
	 	class B extends A    //father
	 	{
	 	
	 	}
	 	
	 	class C extends B	//son
	 	{
	 	
	 	}
	 				
	 	
	 */

}
