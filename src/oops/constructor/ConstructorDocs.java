package oops.constructor;

public class ConstructorDocs {
	
/*
 * ================Constructor =====================
 * #Used to create object.
 * #special type of method which has same name as class name is known as constructor.
 * #does not have return type.
 * #if there is no any user defined constructor in class then java provides default constructor.
 * 
 * ================Types of Constructor===============
 * 
 * 1> Default Constructor:
 * 		#used to create object and set default values to it(object).
 * 		
 * 			syntax:
 * 		class_name(){
 * 
 * 			//set default values to object
 * 
 * 		}
 * 
 * 2> Parameterized Constructor:
 * 		#used to create object and set dynamic values to it(object).
 * 
 * 			syntax:
 * 		class_name(arg1, arg2, arg3,......,argn){
 * 
 * 			//set dynamic or user pass values to object.
 * 
 * 		}
 * 	
 * 
 */

}
